
function add() {
  return a + b;
}

function subtract() {
  return a - b;
}

function multiply() {
  return a * b;
}

function divide() {
  return a / b;
}

function inc(n) {
  return a++;
}

function dec(n) {
  return a--;
}

function makeInt(n) {
  return parseInt(a);
}

function preserveDecimal(n) {
  return parseFloat(a);
}